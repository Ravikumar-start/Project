package com.practiceexamples;

public class SwappingOfNumbers {

	public static void main(String[] args) {
		
	int x=10;
	
	int y=20;
	
	
	
	int temp=x;
	
	x=y;
	
	y=temp;
	
	System.out.println(x);
	System.out.println(y);
	
	
	//**************************************************************
	x=10;
	y=20;
	
	x=x+y;//30
	
	y=x-y;//10
	x=x-y;//20
	System.out.println(x);
	System.out.println(y);
	
	
	
	}

}
